// Bakes an accurate dot-matrix land mask from Natural Earth (world-atlas
// land-110m) into engine/dotmap-data.ts. Samples a lon/lat grid via
// d3.geoContains so continents are real, not hand-guessed. Run: npx tsx
// scripts/bake-dotmap.ts — then the renderer imports the static data (no
// runtime geo dependency).
import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { geoContains } from "d3-geo";
import { feature } from "topojson-client";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");

const topo = JSON.parse(
  await readFile(resolve(repoRoot, "node_modules/world-atlas/land-110m.json"), "utf8"),
);
const land = feature(topo, topo.objects.land) as GeoJSON.Feature;

// Grid: longitude full 360°, latitude cropped to drop Antarctica + far north
// noise (matches editorial dotted-map convention). ~square cells.
const COLS = 104;
const LAT_TOP = 83;
const LAT_BOTTOM = -56;
const LAT_SPAN = LAT_TOP - LAT_BOTTOM;
const ROWS = Math.round((COLS * LAT_SPAN) / 360);

const ranges: Record<number, [number, number][]> = {};
for (let r = 0; r < ROWS; r++) {
  const lat = LAT_TOP - ((r + 0.5) / ROWS) * LAT_SPAN;
  let run: number | null = null;
  const rowRanges: [number, number][] = [];
  for (let c = 0; c < COLS; c++) {
    const lon = -180 + ((c + 0.5) / COLS) * 360;
    const isLand = geoContains(land, [lon, lat]);
    if (isLand && run === null) run = c;
    if (!isLand && run !== null) {
      rowRanges.push([run, c - 1]);
      run = null;
    }
  }
  if (run !== null) rowRanges.push([run, COLS - 1]);
  if (rowRanges.length) ranges[r] = rowRanges;
}

const totalDots = Object.values(ranges)
  .flat()
  .reduce((a, [s, e]) => a + (e - s + 1), 0);

const out = `// AUTO-GENERATED by scripts/bake-dotmap.ts — do not edit by hand.
// Equirectangular land mask sampled from Natural Earth (world-atlas land-110m).
// ${COLS} cols (lon −180..180) × ${ROWS} rows (lat ${LAT_TOP}..${LAT_BOTTOM}), ${totalDots} land cells.
export const DOTMAP_COLS = ${COLS};
export const DOTMAP_ROWS = ${ROWS};
export const DOTMAP_LAT_TOP = ${LAT_TOP};
export const DOTMAP_LAT_BOTTOM = ${LAT_BOTTOM};
export const DOTMAP_LAND: Record<number, [number, number][]> = ${JSON.stringify(ranges)};
`;

await writeFile(resolve(repoRoot, "engine/dotmap-data.ts"), out, "utf8");
console.log(`Baked ${COLS}×${ROWS}, ${totalDots} land dots → engine/dotmap-data.ts`);
